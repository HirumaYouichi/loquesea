// Funciones

// Función que muestra un mensaje en la consola
// recibe un mensaje como parámetro
function mostrarMensaje(mensaje) {
  console.log(mensaje);
}

// Función que suma dos números
// recibe dos números como parámetros
// regresa la suma de los dos números
function suma(a, b) {
  return a + b;
}

// Función que resta dos números
// recibe dos números como parámetros
// regresa la resta de los dos números
function resta(a, b) {
  return a - b;
}
